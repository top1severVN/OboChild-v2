class solve():
    """đây là class các công thức tính hóa học"""
    def mol(m, M):
        """n: số mol, m: khối lượng, M: khối lượng mol"""
        return m / M
    def mass(n, M):
        """m: khối lượng, n: số mol, M: khối lượng mol"""
        return n * M
    def molar_mass(m, n):
        """M: khối lượng mol, m: khối lượng, n: số mol"""
        return m / n
    def empirical_formula(mass, molar_mass):
        """công thức phân tử, công thức thực nghiệm, khối lượng mol, khối lượng"""
        return mass / molar_mass
    def percent_composition(mass, total_mass):
        """phần trăm thành phần, khối lượng, tổng khối lượng"""
        return (mass / total_mass) * 100
    def limiting_reagent(moles, coefficients):
        """chất phản ứng giới hạn, số mol, hệ số"""
        return min(moles[i] / coefficients[i] for i in range(len(moles)))
    def theoretical_yield(limiting_reagent, molar_mass_product):
        """năng suất lý thuyết, chất phản ứng giới hạn, khối lượng mol sản phẩm"""
        return limiting_reagent * molar_mass_product
    def cmol(v, M):
        """số mol, thể tích, khối lượng mol"""
        return v * M
    def molar_concentration(n, V):
        """n: số mol, V: thể tích"""
        return n / V
    def dilution(c1, v1, c2):
        """c1: nồng độ ban đầu, v1: thể tích ban đầu, c2: nồng độ sau khi pha loãng"""
        return (c1 * v1) / c2
    def gas_law(P, V, n, R, T):
        """P: áp suất, V: thể tích, n: số mol, R: hằng số khí, T: nhiệt độ"""
        return (P * V) / (n * R * T)
    def energy_mass(m):
        """E: năng lượng, m: khối lượng, c: tốc độ ánh sáng"""
        return m * (3 * 10**8) ** 2
    def half_life(t, k):
        """t: thời gian, k: hằng số phân rã"""
        return t / k
    def gas_bolt_avo(r, k):
        """r: hằng số khí lý tưởng, k: hằng số Boltzmann"""
        return r / k
    def avogadro_number(n, V):
        """n: số mol, V: thể tích"""
        return n / V
    def newton2(m, a):
        """F: lực, m: khối lượng, a: gia tốc"""
        return m * a
    def newton3(m1, m2, r):
        """F: lực hấp dẫn, m1: khối lượng thứ nhất, m2: khối lượng thứ hai, r: khoảng cách giữa hai khối lượng"""
        G = 6.67430 * 10**-11
        return G * (m1 * m2) / r**2
    def kinetic_energy(m, v):
        """KE: động năng, m: khối lượng, v: vận tốc"""
        return 0.5 * m * v**2
    def potential_energy(m, g, h):
        """PE: thế năng, m: khối lượng, g: gia tốc trọng trường, h: độ cao"""
        return m * g * h
    def work(F, d):
        """W: công, F: lực, d: khoảng cách"""
        return F * d
    def power(W, t):
        """P: công suất, W: công, t: thời gian"""
        return W / t
    

